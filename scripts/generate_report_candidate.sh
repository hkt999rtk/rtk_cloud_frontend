#!/usr/bin/env bash
set -euo pipefail

profile="${1:-}"
output="${2:-}"

if [[ -z "$profile" || -z "$output" ]]; then
  echo "usage: $0 <ci|cd> <output-path>" >&2
  exit 2
fi

result_or_pass() {
  local value="${1:-}"
  if [[ -z "$value" ]]; then
    echo "PASS"
  else
    echo "$value"
  fi
}

count_status() {
  local want="$1"
  shift
  local count=0
  local value
  for value in "$@"; do
    if [[ "$value" == "$want" ]]; then
      count=$((count + 1))
    fi
  done
  echo "$count"
}

overall_result() {
  local value
  for value in "$@"; do
    if [[ "$value" == "FAIL" ]]; then
      echo "FAIL"
      return
    fi
  done
  for value in "$@"; do
    if [[ "$value" == "BLOCKED" ]]; then
      echo "BLOCKED"
      return
    fi
  done
  echo "PASS"
}

write_common_header() {
  local title="$1"
  local report_class="$2"
  local overall="$3"
  local scope="$4"

  cat <<EOF
# ${title}

## Summary

- Report ID: ${REPORT_ID:-deterministic-candidate}
- Created: ${REPORT_CREATED:-deterministic candidate generated by workflow}
- Prepared by: ${REPORT_PREPARED_BY:-GitHub Actions}
- Automation identity: ${REPORT_AUTOMATION_IDENTITY:-github-actions}
- Repository: hkt999rtk/rtk_cloud_frontend
- Report class: ${report_class}
- Overall result: ${overall}
- Scope: ${scope}

## Source Anchors

- Repository commit: ${REPORT_REPOSITORY_COMMIT:-current workflow checkout; see GitHub Actions run metadata}
