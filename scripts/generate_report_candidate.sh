- Credentials source: CI secret
- Secret handling note: report candidates contain sanitized command names and result states only; raw logs stay in workflow logs or non-report artifacts.

## Commands

\`\`\`sh
${REPORT_COMMANDS}
\`\`\`

## Result Summary

EOF
}

write_tail() {
  local report_path="$1"
  local raw_dir="$2"
  local artifact_dir="$3"

  cat <<EOF
